        private void generateTable() throws SQLException{
            gm.dadosmulta();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            for(Generatemulta m : gm.multa){
                model.addRow(new Object[]{
                    m.getIdmulta(),
                    m.getMotivo(),
                    m.getValor(),
                    m.getTipopago(),
                    m.getMetodopag(),
                    m.getUserid(),
                    m.getUserbi(),
                    m.getUsernif(),
                    m.getMatricula(),
                    m.getNumlugar(),
                    m.getIdest(),
                    m.getCodzona(),
                    m.getDatainicio(),
                    m.getDatafim(),
                });
                DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment( JLabel.CENTER );
                jTable1.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(5).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(9).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(10).setCellRenderer(centerRenderer);
                jTable1.getColumnModel().getColumn(11).setCellRenderer(centerRenderer);
            }
        }
    

